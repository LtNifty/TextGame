package gameData.Skills;

public class Prayer extends Skill {

	public Prayer(int lvl_) {
		super(lvl_);
		this.name = "Prayer";
		this.desc = "A skill that affects your ability to use holy buffs and their duration.";
	}
	
	public Prayer(double xp_) {
		super(xp_);
		this.name = "Prayer";
		this.desc = "A skill that affects your ability to use holy buffs and their duration.";
	}
	
}
